#ifndef __STDINT_H__
#define __STDINT_H__ 0x1

/* #pragma once */

/* Integer */

/* 8 bit (1 byte) */
typedef char int8_t;
/* 16 bit (2 byte) */
typedef short int16_t;
/* 32 bit (4 byte) */
typedef int int32_t;
/* 64 bit (8 byte) */
typedef long int64_t;

/* Signed Integer */

/* 8 bit signed (1 byte) */
typedef signed char sint8_t;
/* 16 bit signed (2 byte) */
typedef signed short sint16_t;
/* 32 bit signed (4 byte) */
typedef signed int sint32_t;
/* 64 bit signed (8 byte) */
typedef signed long sint64_t;

/* Unsigned Integer */

/* 8 bit unsigned (1 byte) */
typedef unsigned char uint8_t;
/* 16 bit unsigned (2 byte) */
typedef unsigned short uint16_t;
/* 32 bit unsigned (4 byte) */
typedef unsigned int uint32_t;
/* 64 bit unsigned (8 byte) */
typedef unsigned long uint64_t;

#endif /* __STDINT_H__ */